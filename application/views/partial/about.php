		<p>We met each other on May 19, 2014 at the Global Hackathon in Dongdaemun Design Plaza in Seoul, Korea, sponsored by AppCenter and SBS Foundation. Within a span of less than 24 hours, we brainstormed, conceptualized and launched <strong>LangHack</strong>.</p>
		<p><strong>LangHack</strong> is a platform that connects language learners with natives wherever they are.</p>
		<p>Most language learning is one-sided, expensive, time consuming, and just not fun.</p>
		<p>We believe learners should connect in person with real people, for a true language exchange, while building deeper relationships and creating meaningful and memorable experiences.</p>
